public class OperasiPerbandingan {
  public static void main(String[] args) {
    int f = 100;
    int g = 90;
    int h = 90;

    System.out.println(f > g);
    System.out.println(f < g);
    System.out.println(g >= h);
    System.out.println(g <= h);
    System.out.println(g == h);
    System.out.println(g != h);
  }
}
